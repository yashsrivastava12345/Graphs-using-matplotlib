import matplotlib.pyplot as plt
import random

a=random.randint(0,100)
b=random.randint(0,100)
c=random.randint(0,100)
d=random.randint(0,100)
e=random.randint(0,100)
sizes = [a,b,c,d,e ]
plt.pie(sizes,labels = {'Yash', 'Shruti', 'Adity', 'Tapan','Sanjeet'},colors = ['m', 'r', 'g', 'b','olive'],autopct ='% 1.1f %%', shadow = False)
plt.title("collection chart of 100 rupees for first year")
#plt.legend(patches, labels, loc="best")
plt.show() 
